						<h3>About</h3>
						<img src="images/theMissionImg.jpg" alt="Staff member working in the fields.">
						<p>
							Next Barn Over Farm is a 34 acre mixed vegetable farm in Hadley, MA at the foot of the Holyoke Range  and near the confluence of the Fort and Connecticut  Rivers. We grow over <a class="colorLink" href="index.php?p=ourCrops">forty different crops</a>, primarily for our <a class="colorLink" href="index.php?p=onFarmShare">on-farm</a> CSA, as well as for <a class="colorLink" href="index.php?p=offFarmShare">off-farm</a> box-shares and a few grocery store and restaurants. We generally work with a crew of seven in the fields as well as a membership coordinator and a shareroom manager (and since each of these exceptional folks tend to pull the weight of two, we manage to get it all done). <a class="colorLink" href="thePeople.html">Ray</a> and <a class="colorLink" href="index.php?p=thePeople">Tory</a> started Next Barn Over in 2010 building on the tradition and community of Food Bank Farm where they apprenticed with Michael Doctor.
						</p>
						<p>
							Once the rich bottomland of ancient Lake Hitchcock, archeological research suggests that the fertile valley we are in has been farmed for several thousand years. In addition to the corn-beans-and-squash that was grown here for millennia by Native Americans prior to the arrival of European immigrants, the last several hundred years have seen a variety of farming primarily dairy, hay, tobacco, winter squash, potatoes, corn, asparagus, and other vegetables. Hadley loam, Hadley asparagus, and Hadley corn have distinguished this otherwise quiet New England town.
						</p>
						<p>
							Because of its rich soil and strong agricultural industry, Hadley has the most farmland in Agricultural Protection Restriction (APR) in the state of Massachusetts, with over 2,400 acres protected from development. The APR program has helped farming remain viable and new farms like ours possible. Almost all of the land that we farm is preserved for agricultural use forever.
						</p>
