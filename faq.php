			<section id="schedule" class="secondaryPageSubSections">
				<h3 id="employmentFAQ">Employment FAQ</h3>
				<h4>What is the schedule like?</h4>
				<p>
					Our work week ranges from 40-60 hours and the schedule varies throughout the season. While the spring and fall will carry a lighter load, during the height of the season, we will work from 6 am to approximately 6 pm on Monday through Friday and from 7am to approximately 1pm on Saturdays. We will take a half hour-long lunch together each day. During the months that we work Saturdays, everybody has one Saturday off per month.
				</p>
				<h4 id="compensation">What is the compensation for apprentices?</h4>
				<p>
					Apprentices are given a monthly stipend, based on experience, from April through October. Apprentices have unlimited access to farm produce and may purchase other shareroom items (dairy, bread, meat, etc) at the wholesale price that we pay for it. Apprentices are eligible for Massachusettsâ€™ free health care program. No housing is provided.
				</p>
				<h4 id="orientation">Is there an orientation beforehand?</h4>
				<p>
					At the beginning of the season the crew meets to go over farm systems, policies, areas of work, and safety guidelines. At this meeting we also assign farm responsibilities and tractors. All apprentices will be provided a manual with written materials that cover all aspects of the farm, including:  field maps, crop plans/ planting schedules, seed orders, budgets, tractor information, etc. 
				</p>
				<p>
					Orientation to farm systems is an ongoing process and the assistant manager helps the crew learn new tasks throughout the season. When new tasks are introduced the assistant manager will take the time to explain and demonstrate the activity to everyone. The assistant manager will monitor folks performance and give them feedback and tips to improve their speed, quality, or ergonomics.
				</p>
				<p>
					There will be space for questions and discussion about systems in the day to day while weeding and over lunch, however often the time for longer conversations will be in the beginning and end of the season when field work is slower.
				</p>
				<h4 id="evaluation">How are we evaluated?</h4>
				<p>
					At the beginning of each season, each apprentice will meet individually with the farm manager to discuss expectations and personal goals for the season, which will be recorded in an individual contract. Seasonal meetings will be held with each apprentice to track how the goals are being met. The first meeting with the farm manager will happen sometime between July 1 and August 15. The final meeting will happen sometime during the month of October. The evaluation will be multi-directional and apprentices will be given the opportunity to give feed back on their experience as well as receiving feedback on their performance.
				</p>
