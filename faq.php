			<section id="csaAbout" class="secondaryPageSubSections">
				<h3 id="csaFAQ">Community Supported Agriculture FAQ</h3>
				<h4>What is community supported agriculture?</h4>
				<p>
					Community Supported Agriculture (CSA) brings together community members, farmers, and farmland in a mutually beneficial relationship that sustains local agriculture, ensures greater food security, and strengthens communities.
				</p>
				<h4 id="buyShare">Why should I buy a share?</h4>
				<p>
					By purchasing a share of the harvest in advance, CSA customer-members pay below the retail market value and receive fresh produce weekly throughout the growing season. Farmers receive working capital at the beginning of the season when they need it most and are ensured a steady market independent of seasonal fluctuations. Farmers and customers both benefit from the opportunity to build direct relationships with each other and to form communities based on mutual aid and a shared connection to that which sustains us.
				</p>
				<h4 id="shareDifference">What are the different types of shares available?</h4>
				<p>
					We offer <a href="index.php?p=onFarmShare" class="colorLink">on-farm shares</a> and <a href="index.php?p=offFarmShare" class="colorLink">off-farm shares</a>. On-farm shares allow members to come to the farm to pick their produce, while off-farm shares are pre-packaged shares that can be picked up at several different locations. 
				</p>
