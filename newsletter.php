						<h3 id="newsletterRecipes">Recipes</h3>
						<h4>Braised Fennel with White Beans</h4>
						<img src="images/recipe1Img.jpg">
						<p>
							Heat oil in a large, heavy saute pan over medium-high heat. Add fennel and onion; cook, stirring occasionally, until tender and edges are brown, about 10 minutes.
						</p>
						<p>
							Add beans, stock, chopped oregano, 1 teaspoon salt, and 1/4 teaspoon pepper. Cook, stirring occasionally, until liquid has reduced by about half, 3 to 4 minutes. Stir in vinegar and butter; remove from heat. Garnish with oregano leaves. 
						</p>
						<p class="noMargin">Ingredients</p>
							<ul class="ulList">
								<li>2 tablespoons extra-virgin olive oil ;</li>
								<li>1 medium fennel bulb, trimmed, halved lengthwise and cut crosswise into 1/4-inch-thick slices (about 3 cups);</li>
								<li>1 medium red onion, halved lengthwise and cut lengthwise into 1/4-inch-thick slices (about 2 cups) ;</li>
								<li>1 can (15 1/2 ounces) white beans;</li>
								<li>1 cup homemade or store-bought chicken stock;</li>
								<li>2 teaspoons coarsely chopped fresh oregano, plus whole leaves for garnish;</li>
								<li>Coarse salt and freshly ground pepper;</li>
								<li>2 tablespoons red-wine vinegar;</li>
								<li>1 tablespoon unsalted butter, softened.</li>
							</ul>
						<h4>Turnip Hash with Broccoli Raab</h4>
						<img src="images/recipe2Img.jpg">
						<p>
							Bring a large pot of water to a boil. Add turnips to pot; boil until just tender when pierced with a fork, 3 to 5 minutes. Using a slotted spoon, transfer the turnips to a colander to drain. Repeat process with sweet potatoes, then potatoes. Add broccoli rabe to pot, and boil until bright green and crisp-tender, about 1 minute. Drain in colander; set aside. 
						</p>
						<p>
							Heat oil in a large, heavy skillet over medium heat until hot but not smoking. Add onion, garlic, salt, red-pepper flakes, thyme, and reserved turnips, sweet potatoes, and potatoes; spread evenly to cover bottom of skillet. Cook, without stirring, until vegetables begin to brown on bottom, about 15 minutes. 
						</p>
						<p>
							Add broccoli rabe to skillet. Stir once; cook until vegetables are very tender and browned, about 25 minutes. Serve hot.
						</p>
						<p class="noMargin">Ingredients</p>
							<ul class="ulList">
								<li>1/2 pound medium turnips, peeled and cut into 1/2-inch dice;</li>
								<li>1/2 pound sweet potatoes, peeled and cut into 1/2-inch dice;</li>
								<li>1/2 pound potatoes, peeled and cut into 1/2-inch dice;</li>
								<li>1 bunch broccoli rabe, cut into 1/2-inch pieces (about 6 1/2 cups);</li>
								<li>1 1/2 tablespoons extra-virgin olive oil;</li>
								<li>1 medium onion, coarsely chopped;</li>
								<li>4 garlic cloves, smashed;</li>
								<li>3/4 teaspoon coarse salt;</li>
								<li>1/4 teaspoon crushed red-pepper flakes;</li>
								<li>1 teaspoon coarsely chopped fresh thyme.</li>
							</ul>
