						<h3 id="newsletterStorage">How to Store Vegetables</h3>
						<p>
							If you're curious about where you might put 100 lbs of veggies, here is the quick version.
						</p>
						<p>
							Roots (carrots, beets, turnips, celeriac, parsnips, radishes, etc.), and Cabbage  &#8211; like cold, damp, and dark. But they don't want to freeze. 
						</p>
						<p class="noMargin">
							Some places to keep them:
						</p>
							<ul class="ulList">
								<li>Your fridge! Easiest, if you can find the room;</li>
								<li>An unheated portion of your basement, mudrrom, or other cold room in your house;</li>
								<li>A garage with blankets thrown over them to prevent freezing.</li>
							</ul>
						<p>
							Onions and garlic &#8211; like someplace dry, dark, and cool. The light will trigger their innate desire to grow and sprout. Easiest is to keep them in a paper bag in a cupboard down low somewhere in your kitchen (the down low cupboards don’t get as toasty since heat rises).
						</p>
						<p>
							Squash, potatoes, and sweet potatoes like dry, dark and room temperature. In a paper bag in a cupboard or on your countertop is fine (squash doesn't need to be dark... it can be naked on your counter).
						</p>
						<p>
							Greens, Broccoli, and Cauliflower &#8211; these you just need to eat. Or… you can chop, quickly blanch, and freeze them so they last through the winter.  
						</p>
