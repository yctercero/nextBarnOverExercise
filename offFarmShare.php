						<h3>Membership</h3>
						<img src="images/offFarmImg2.jpg" alt="Member surrounded by off-farm pre-packaged shares.">
						<p>
							To become a CSA sharemember for the 2015 growing season you can either use our handy <a href="index.php?p=signUp" class="colorLink">online registration form</a> or you can download and fill out the <a href="documents/NextBarnOver-SignUp2015.pdf" target="_blank" class="colorLink">sign-up sheet form</a>. Members can pay up front or choose a payment schedule for their shares. Folks may share a CSA share between two households by either coming together or alternating pick up weeks. We also offer an 'every-other-week' share for members who want a smaller amount of vegetables. Members pick up the same amount and type of vegetables when they come to the farm, but only come every other week. We do accept SNAP/food stamp benefits for farm shares (please contact <a href="mailto:membership@nextbarnover.com" class="colorLink">membership@nextbarnover.com</a> for more info). 
						</p>
