						<h3>What is an off-farm share?</h3>
						<img src="images/offFarmImg.jpg" alt="Gardening the Community members with off-farm pre-packaged shares.">
						<p>
							We deliver weekly pre-boxed shares to specific sites in Springfield, Westfield, and Boston. We offer two different sizes of box shares. Full share members receive a bushel (~15-20 lbs or enough for 2-4 people) and half-share members receive a half-bushel (~8-10 lbs or enough for 1-2 people).
						</p>
						<p>
							As a mixed vegetable CSA farm, we grow over <a href="index.php?p=ourCrops" class="colorLink">forty different crops</a> throughout the harvest season, from June through October. The selection changes as the season progresses, though we include an abundance of "the basics" all season long. A typical week includes a bag of mixed salad greens, a head of lettuce, a bunch of cooking greens, onions and/or garlic, carrots, herbs, and a number of seasonal favorites depending on the time of year (tomatoes, cucumbers, melons, zucchini, peppers, sweet potatoes, broccoli, winter squash, eggplant, cauliflower, and a well-varied smattering of the less common crops). Each week we send out an email <a href="index.php?p=newsletter" class="colorLink">newsletter</a> that describes what is in the share along with farm news, recipes, sustainable agriculture information, and invitations to <a href="index.php?p=events" class="colorLink">farm events</a>. For more information about the specific sites, please see below. 
						</p>
						<p>
							If your interested in setting up a farmshare distribution site in your town, school, or workplace, please be in touch with us at <a href="mailto:info@nextbarnover.com" class="colorLink">info@nextbarnover.com</a>.
						</p>
